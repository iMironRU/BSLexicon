/**
 * Курированные lifecycle-сценарии 1С: «что когда срабатывает» при
 * типичных пользовательских действиях (запись документа, проведение,
 * открытие формы и т.п.).
 *
 * Источник — наше педагогическое знание, **не** тексты синтакс-помощника
 * 1С (по лицензии нельзя). 1С нигде не публикует «порядок срабатывания»
 * структурированно — это инсайдерское знание разработчиков платформы.
 * Здесь только короткие пояснения от себя, имена событий — точные из
 * выгрузки, чтобы клик уводил на их карточку.
 */

export type SwimlaneId = 'client' | 'server' | 'object';

export interface Swimlane {
  id: SwimlaneId;
  label: string;
}

export interface LifecycleStep {
  /** Уникальный id шага в сценарии (для якорей/ссылок). */
  id: string;
  /** К какой дорожке относится. */
  swimlane: SwimlaneId;
  /** Имя события из выгрузки в формате `owner.nameRu` — для deep-link. */
  event: string;
  /** Короткая подпись для блока на диаграмме (если имя owner-а длинное). */
  displayName?: string;
  /** Свободный пояснительный текст (наша педагогика). */
  note?: string;
  /** Если шаг может прерваться `Отказ = Истина` — отдельная ветвь. */
  cancel?: string;
}

export interface LifecycleScenario {
  /** id для URL: `#/lifecycle/<id>`. */
  id: string;
  /** Человекочитаемое название. */
  title: string;
  /** Короткое введение. */
  description: string;
  swimlanes: Swimlane[];
  steps: LifecycleStep[];
}

