import { describe, expect, it } from 'vitest';
import { run } from '@core/index';

/** Удобный помощник: запускает код и возвращает вывод одной строкой. */
function output(source: string): string {
  const result = run(source);
  if (!result.ok) {
    throw new Error(`${result.error.stage}: ${result.error.message}`);
  }
  return result.output.join('\n');
}

describe('арифметика и присваивание', () => {
  it('считает выражение и выводит результат', () => {
    expect(output('А = 2 + 2 * 2; Сообщить(А);')).toBe('6');
  });

  it('учитывает приоритет скобок', () => {
    expect(output('Сообщить((2 + 2) * 2);')).toBe('8');
  });

  it('конкатенирует строку с числом', () => {
    expect(output('Сообщить("Итог=" + 5);')).toBe('Итог=5');
  });
});

describe('управление потоком', () => {
  it('выбирает ветку Если/Иначе', () => {
    expect(output('Если 3 > 2 Тогда Сообщить("да"); Иначе Сообщить("нет"); КонецЕсли;')).toBe('да');
  });

  it('суммирует в цикле Для', () => {
    const code = `
      Итог = 0;
      Для Сч = 1 По 5 Цикл
        Итог = Итог + Сч;
      КонецЦикла;
      Сообщить(Итог);
    `;
    expect(output(code)).toBe('15');
  });

  it('обрабатывает Прервать в цикле Пока', () => {
    const code = `
      Сч = 0;
      Пока Истина Цикл
        Сч = Сч + 1;
        Если Сч >= 3 Тогда Прервать; КонецЕсли;
      КонецЦикла;
      Сообщить(Сч);
    `;
    expect(output(code)).toBe('3');
  });
});

describe('переменная, меняющая тип в рантайме', () => {
  it('инспекция отражает текущий тип', () => {
    const result = run('Х = 1; Х = "строка";');
    if (!result.ok) throw new Error(result.error.message);
    const x = result.variables.find((v) => v.name === 'Х');
    expect(x?.type).toBe('Строка');
  });
});

describe('пользовательские процедуры и функции', () => {
  it('вызывает функцию и возвращает значение', () => {
    const code = `
      Функция Удвоить(Знач Х)
        Возврат Х * 2;
      КонецФункции
      Сообщить(Удвоить(21));
    `;
    expect(output(code)).toBe('42');
  });

  it('применяет значение параметра по умолчанию', () => {
    const code = `
      Функция Привет(Имя = "мир")
        Возврат "Привет, " + Имя;
      КонецФункции
      Сообщить(Привет());
    `;
    expect(output(code)).toBe('Привет, мир');
  });
});

describe('двуязычность и регистронезависимость', () => {
  it('понимает английские ключевые слова', () => {
    expect(output('If 1 = 1 Then Сообщить("ok"); EndIf;')).toBe('ok');
  });

  it('регистронезависим к идентификаторам', () => {
    expect(output('Перем Итог; итог = 7; Сообщить(ИТОГ);')).toBe('7');
  });
});

describe('ошибки', () => {
  it('сообщает об ошибке выполнения с этапом', () => {
    const result = run('Сообщить(1 / 0);');
    expect(result.ok).toBe(false);
    if (!result.ok) expect(result.error.stage).toBe('runtime');
  });

  it('сообщает о синтаксической ошибке', () => {
    const result = run('Если 1 > 0 Сообщить("нет Тогда");');
    expect(result.ok).toBe(false);
    if (!result.ok) expect(result.error.stage).toBe('parser');
  });
});

describe('Массив', () => {
  it('создаётся, наполняется и читается по индексу', () => {
    const code = `
      М = Новый Массив;
      М.Добавить(1); М.Добавить(2); М.Добавить(3);
      Сообщить(М.Количество());
      Сообщить(М[0] + М.Получить(2));
    `;
    expect(output(code)).toBe('3\n4');
  });

  it('создаётся с размером и поддерживает Установить', () => {
    const code = `
      М = Новый Массив(2);
      М[0] = "а";
      М.Установить(1, "б");
      Сообщить(М[0] + М[1]);
    `;
    expect(output(code)).toBe('аб');
  });

  it('обходится «Для Каждого»', () => {
    const code = `
      М = Новый Массив;
      М.Добавить(10); М.Добавить(20);
      Итог = 0;
      Для Каждого Э Из М Цикл
        Итог = Итог + Э;
      КонецЦикла;
      Сообщить(Итог);
    `;
    expect(output(code)).toBe('30');
  });

  it('ошибка при выходе за границы', () => {
    const result = run('М = Новый Массив; Сообщить(М[0]);');
    expect(result.ok).toBe(false);
    if (!result.ok) expect(result.error.stage).toBe('runtime');
  });
});

describe('Структура', () => {
  it('создаётся с ключами и читается через точку', () => {
    const code = `
      С = Новый Структура("Имя, Возраст", "Иван", 30);
      Сообщить(С.Имя + ": " + С.Возраст);
    `;
    expect(output(code)).toBe('Иван: 30');
  });

  it('Вставить добавляет свойство, присваивание меняет его', () => {
    const code = `
      С = Новый Структура;
      С.Вставить("x", 1);
      С.x = 5;
      Сообщить(С.x);
      Сообщить(С.Количество());
    `;
    expect(output(code)).toBe('5\n1');
  });

  it('ошибка при присваивании несуществующему свойству', () => {
    const result = run('С = Новый Структура; С.Нет = 1;');
    expect(result.ok).toBe(false);
    if (!result.ok) expect(result.error.stage).toBe('runtime');
  });
});

describe('Соответствие', () => {
  it('доступ по ключу через [ ]; отсутствующий ключ — Неопределено', () => {
    const code = `
      С = Новый Соответствие;
      С["a"] = 1;
      С.Вставить("b", 2);
      Сообщить(С["a"] + С["b"]);
      Сообщить(ТипЗнч(С["нет"]));
    `;
    expect(output(code)).toBe('3\nНеопределено');
  });

  it('обход «Для Каждого» даёт КлючИЗначение', () => {
    const code = `
      С = Новый Соответствие;
      С.Вставить("a", 10);
      С.Вставить("b", 20);
      Сумма = 0;
      Для Каждого КЗ Из С Цикл
        Сумма = Сумма + КЗ.Значение;
      КонецЦикла;
      Сообщить(Сумма);
    `;
    expect(output(code)).toBe('30');
  });
});

describe('ссылочная семантика коллекций', () => {
  it('две переменные делят один массив', () => {
    const code = `
      А = Новый Массив;
      А.Добавить(1);
      Б = А;
      Б.Добавить(2);
      Сообщить(А.Количество());
    `;
    expect(output(code)).toBe('2');
  });

  it('ТипЗнч и инспекция отражают тип коллекции', () => {
    const result = run('М = Новый Массив; М.Добавить(1);');
    if (!result.ok) throw new Error(result.error.message);
    const m = result.variables.find((v) => v.name === 'М');
    expect(m?.type).toBe('Массив');
    expect(m?.display).toContain('Массив [');
  });

  it('параметр без «Знач» — мутация коллекции видна снаружи (по ссылке)', () => {
    const code = `
      Процедура Доб(М)
        М.Добавить(1);
      КонецПроцедуры
      А = Новый Массив;
      Доб(А);
      Сообщить(А.Количество());
    `;
    expect(output(code)).toBe('1');
  });

  it('параметр с «Знач» — коллекция копируется, оригинал не меняется', () => {
    const code = `
      Процедура Доб(Знач М)
        М.Добавить(1);
      КонецПроцедуры
      А = Новый Массив;
      Доб(А);
      Сообщить(А.Количество());
    `;
    expect(output(code)).toBe('0');
  });

  it('«Знач» копирует глубоко — вложенная коллекция не протекает', () => {
    const code = `
      Процедура Порть(Знач Внеш)
        Внеш[0].Добавить(99);
      КонецПроцедуры
      Внутр = Новый Массив;
      Снаружи = Новый Массив;
      Снаружи.Добавить(Внутр);
      Порть(Снаружи);
      Сообщить(Внутр.Количество());
    `;
    expect(output(code)).toBe('0');
  });

  it('«Знач» для структуры — копия, оригинал не тронут', () => {
    const code = `
      Процедура Поменяй(Знач С)
        С.Вставить("Ключ", 2);
      КонецПроцедуры
      С = Новый Структура;
      С.Вставить("Ключ", 1);
      Поменяй(С);
      Сообщить(С.Ключ);
    `;
    expect(output(code)).toBe('1');
  });

  it('«Знач» с циклической ссылкой не зацикливается', () => {
    const code = `
      Процедура Тронь(Знач М)
        М.Добавить(1);
      КонецПроцедуры
      А = Новый Массив;
      А.Добавить(А);
      Тронь(А);
      Сообщить(А.Количество());
    `;
    expect(output(code)).toBe('1');
  });
});
